// ============================================================
// IDIOMA — diccionario compartido (Catálogo, Admin, Mis pedidos).
// El usuario cambia el idioma desde "Mi perfil" > Idioma/Language
// en el menú de perfil (profile-menu.js). Al cambiar, se guarda en
// localStorage y se recarga la página para que todo (textos fijos
// y contenido dinámico) se pinte ya en el idioma elegido.
// ============================================================
const IDIOMA_KEY = 'catalogo-bimbo-idioma';

function obtenerIdioma() {
  return localStorage.getItem(IDIOMA_KEY) === 'en' ? 'en' : 'es';
}

function establecerIdioma(idioma) {
  localStorage.setItem(IDIOMA_KEY, idioma === 'en' ? 'en' : 'es');
  location.reload();
}

