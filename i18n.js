// Nombres propios de cadena (Wawa, 7-Eleven, Circle K, Dash-In) se ven
// igual en ambos idiomas — solo "Independientes" se traduce.
function labelCadena(cadena) {
  if (cadena === 'Independientes') return t('cadenaIndependientes');
  return cadena;
}

