function t(key, ...args) {
  const dict = I18N[obtenerIdioma()] || I18N.es;
  const val = dict[key] !== undefined ? dict[key] : I18N.es[key];
  if (typeof val === 'function') return val(...args);
  return val !== undefined ? val : key;
}
